ngx_log_t * ngx_log_init(u_char *filename)
{
   ngx_log_t *log;
   ngx_open_file_t     *ngx_log_file;
   
   log = malloc(sizeof(ngx_log_t));
   ngx_log_file = malloc(sizeof(ngx_open_file_t));


    ngx_log_file->fd = ngx_open_file(filename, NGX_FILE_APPEND,
                                    NGX_FILE_CREATE_OR_OPEN,
                                    NGX_FILE_DEFAULT_ACCESS);

    if (ngx_log_file->fd == NGX_INVALID_FILE) {
        printf("[alert] could not open error log file: \"%s\" failed", filename);
        ngx_log_file->fd = ngx_stderr;
    }
    
    log->file = ngx_log_file;
	
    return log;
}

